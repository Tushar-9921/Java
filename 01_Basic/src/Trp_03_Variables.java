public class Trp_03_Variables {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x * 5);
        System.out.println(x - 5);
        System.out.println(x + 5);
        System.out.println(x / 5);

        int y;   // Declaration
        y = 20;  // Initialization
        System.out.println(y);

        System.out.println(x * y);

        int z = 1;
        System.out.println(z);
        z = 20;
        System.out.println(z);
        z = 2;
        System.out.println(z);


        int p = 5;
        System.out.println(p);
        p = p * 5; // value assign in p AND it is not equal to
        System.out.println(p);
        p = p + 5; // assign
        System.out.println(p);
        p = p - 50;
        System.out.println(p);
        p = p / 2;
        System.out.println(p);

    }
}
